     /**
     * Acción cuando se presiona el botón "Generar RFC".
     * Crea un objeto Tramites con los datos del formulario y genera el RFC.
     * 
     * @param evt evento del botón
     */
    
    private void calcularRFCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularRFCActionPerformed
        // TODO add your handling code here:
        String nom = txtNombre.getText();
    String pa = txtApaterno.getText();
    String ma = txtAmaterno.getText();
    String fecha = txtFecha.getText();
    char sexo = txtSexo.getText().toUpperCase().charAt(0);
    String estado = txtEstado.getText();
    
    Tramites persona = new Tramites(nom, pa, ma, fecha, sexo, estado);
    String rfc = persona.GenerarRFC(nom, pa, ma, fecha);

    etiRFC.setText(rfc);
    }//GEN-LAST:event_calcularRFCActionPerformed

    
