    /**
     * Acción cuando se presiona el botón "Calcular ISR".
     * Calcula el ISR según el tipo de contribuyente y el ingreso introducido.
     * 
     * @param evt evento del botón
     */
    
    
    private void calcularISRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularISRActionPerformed
        // TODO add your handling code here:
        String nom = txtNombre.getText();
    String pa = txtApaterno.getText();
    String ma = txtAmaterno.getText();
    String fecha = txtFecha.getText();
    char sexo = txtSexo.getText().toUpperCase().charAt(0);
    String estado = txtEstado.getText();
         
    

    Tramites persona = new Tramites(nom, pa, ma, fecha, sexo, estado); 
    String rfc = etiRFC.getText();
    double ingreso = Double.parseDouble(txtIngreso.getText());

    
    double isr = persona.calcularISR(rfc, ingreso);
    etiquetaISR.setText(String.valueOf(isr));
    }//GEN-LAST:event_calcularISRActionPerformed

    
